export const getFeaturedJobs = () => {
  return MOCK_JOBS_DATA.filter(job => 
    job.job_employment_type === "Full-time"
  ).slice(0, 6);
};

export const getNearbyJobs = () => {
  return MOCK_JOBS_DATA.filter(job => 
    job.job_city === "Jakarta"
  ).slice(0, 8);
};

export const getJobById = (jobId: string) => {
  return MOCK_JOBS_DATA.find(job => job.job_id === jobId);
};

export const searchJobs = (query: string) => {
  const lowercaseQuery = query.toLowerCase();
  return MOCK_JOBS_DATA.filter(job =>
    job.title.toLowerCase().includes(lowercaseQuery) ||
    job.company_name.toLowerCase().includes(lowercaseQuery) ||
    job.job_description.toLowerCase().includes(lowercaseQuery)
  );
};